package com.cybage.basic;

public class Inheritance 
{
	private String name;
	private String address;
	Inheritance(String name, String address)
	{
		this.name=name;
		this.address=address;
	}
	
	public String getName()
	{
		return this.name;
	}
	
	public String getAddress()
	{
		return this.address;
	}

}
